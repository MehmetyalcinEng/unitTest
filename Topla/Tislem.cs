using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Topla
{
    public class Tislem
    {


        public double toplamaislemi(double num1, double num2)
            {
                return num1 + num2;
            }

        public double cikartmaislemi(double num1,double num2)
        {
            return num1 - num2;
        }


        public double carpmaislemi(double num1, double num2)
        {
            return num1 * num2;
        }

        public double bolmeislemi(double num1, double num2)
        {
            return num1 / num2;
        }
    }
}
